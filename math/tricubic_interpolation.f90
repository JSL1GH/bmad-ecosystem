function tricubic_eval (tri_coef , x_norm, y_norm, z_norm) result (value)

type (tricubic_coef_struct) tri_coef

real(rp) x_norm, y_norm, z_norm, value
real(rp) x_power(0:3), y_power(0:3), z_power(0:3)

integer i, j, k

!

x_power(0) = 1
y_power(0) = 1
z_power(0) = 1

do i = 1, 3
  x_power(i) = x_power(i-1) * x_norm
  y_power(i) = y_power(i-1) * y_norm
  z_power(i) = z_power(i-1) * z_norm
enddo

value = 0
do i = 0, 3
do j = 0, 3
do k = 0, 3
  value = value + x_power(i) * y_power(j) * z_power(k) * tri_coef%coef(i,j,k)
enddo
enddo
enddo

end function tricubic_eval

