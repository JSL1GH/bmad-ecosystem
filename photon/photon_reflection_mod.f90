!---------------------------------------------------------------------------------------------
!---------------------------------------------------------------------------------------------
!---------------------------------------------------------------------------------------------
!+
! Subroutine finalize_reflectivity_tables (surface)
!
! Routine to finalize the construction of the reflectivity tables for a surface.
!
! Input:
!   surface -- photon_reflect_surface_struct: Surface tables to be finalized.
!-

Subroutine finalize_reflectivity_tables (surface)

implicit none

type (photon_reflect_surface_struct), target :: surface
type (photon_reflect_table_struct), pointer :: prt

real(rp) f, deriv, dprob

integer i, j, k

! Take the logiarithm of p_reflect. Where zero, just use 10^-20

do i = 1, size(surface%table)
  prt => surface%table(i)
  do j = 1, size(prt%energy)

    do k = 1, size(prt%angle)
      prt%p_reflect_rough(k, j) = log(max(1.0e-20_rp, prt%p_reflect_rough(k, j)))
      prt%p_reflect_smooth(k, j) = log(max(1.0e-20_rp, prt%p_reflect_smooth(k, j)))
    enddo

    ! First interval interpolation: p = c0 + c1 * angle^n

    deriv = (prt%p_reflect_rough(3,j) - prt%p_reflect_rough(1,j)) / prt%angle(3)
    dprob = prt%p_reflect_rough(2,j) - prt%p_reflect_rough(1,j)
    prt%int1_rough(j)%c0     = prt%p_reflect_rough(1,j)
    prt%int1_rough(j)%n_exp  = deriv * prt%angle(2) / dprob
    prt%int1_rough(j)%c1     = dprob / prt%angle(2)**prt%int1_rough(j)%n_exp

    deriv = (prt%p_reflect_smooth(3,j) - prt%p_reflect_smooth(1,j)) / prt%angle(3)
    dprob = prt%p_reflect_smooth(2,j) - prt%p_reflect_smooth(1,j)
    prt%int1_smooth(j)%c0     = prt%p_reflect_smooth(1,j)
    prt%int1_smooth(j)%n_exp  = deriv * prt%angle(2) / dprob
    prt%int1_smooth(j)%c1     = dprob / prt%angle(2)**prt%int1_smooth(j)%n_exp

  enddo
enddo

end subroutine finalize_reflectivity_tables

